#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	int	i;

	i = 0;
	while (src[i] && 0 < n)
	{
		dst[i] = src[i];
		i += 1;
		--n;
	}
	while  (0 < n--)
		dst[i++] = '\0';
	return (dst);
}
