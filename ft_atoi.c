#include "libft.h"

int		ft_atoi(const char *str)
{
	int	num;

	num = 0;
	while ((*str - '0') >= 0 && (*str - '9') <= 9)
	{
		num *= 10;
		num += (*str - '0');
		++str;
	}
	return (num);
}
